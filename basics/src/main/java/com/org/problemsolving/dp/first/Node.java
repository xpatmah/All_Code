package com.org.problemsolving.dp.first;

public class Node {
  
	private Integer info;
	
	private Node next;

	public Integer getInfo() {
		return info;
	}

	public void setInfo(Integer info) {
		this.info = info;
	}

	public Node getNext() {
		return next;
	}

	public void setNext(Node next) {
		this.next = next;
	}
}
